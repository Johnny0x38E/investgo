const activeLocale = ref<SupportedLocale>(
    normalizeLocale(typeof navigator !== "undefined" ? navigator.language : "zh-CN"),
);

function normalizeLocale(locale: string): SupportedLocale {
    return locale.toLowerCase().startsWith("zh") ? "zh-CN" : "en-US";
}

function resolveLocaleSetting(locale: AppSettings["locale"] | string): SupportedLocale {
    if (locale === "system") {
        return normalizeLocale(typeof navigator !== "undefined" ? navigator.language || "zh-CN" : "zh-CN");
    }
    return normalizeLocale(locale);
}

function resolveMessage(locale: SupportedLocale, key: string): string | undefined {
    const segments = key.split(".");
    let current: string | TranslationTree | undefined = messages[locale];
    for (const segment of segments) {
        if (typeof current !== "object" || current == null || !(segment in current)) {
            return undefined;
        }
        current = current[segment];
    }
    return typeof current === "string" ? current : undefined;
}

function interpolate(message: string, params?: Record<string, string | number>): string {
    if (!params) {
        return message;
    }

    return message.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}

export function setI18nLocale(locale: AppSettings["locale"] | string): void {
    activeLocale.value = resolveLocaleSetting(locale);
}

export function getI18nLocale(): SupportedLocale {
    return activeLocale.value;
}

export function translate(key: string, params?: Record<string, string | number>): string {
    const locale = activeLocale.value;
    const fallback = resolveMessage("zh-CN", key) ?? key;
    const message = resolveMessage(locale, key) ?? fallback;
    return interpolate(message, params);
}

export function useI18n() {
    return {
        locale: readonly(activeLocale),
        isEnglish: computed(() => activeLocale.value === "en-US"),
        t: translate,
    };
}
